void main() {

    printf("       ");
    for (int h = 1; h <= 12; h++) {
        printf("%4d  ", h);
    }

    printf("\n    ---------------------------------------------------------------------------\n");

    for (int i = 1; i <= 12; i++) {

        printf("%2d |   ", i);

        for (int j = 1; j <= 12; j++) {
            printf("%4d  ", i*j);
        }
        printf("|\n");
    }

    printf("    ---------------------------------------------------------------------------\n");

}